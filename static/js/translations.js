// Current language
let currentLanguage = "en"

// Function to update the UI language
function updateLanguage(lang) {
  // Default to English if the language is not supported
  if (!translations[lang]) {
    console.warn(`Language ${lang} not fully supported yet. Falling back to English.`)
    lang = "en"
  }

  currentLanguage = lang
  document.documentElement.lang = lang // Update HTML lang attribute

  // Update all elements with data-translate attribute
  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate")
    if (translations[lang][key]) {
      element.textContent = translations[lang][key]
    }
  })

  // Update placeholders
  document.querySelectorAll("[data-translate-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-translate-placeholder")
    if (translations[lang][key]) {
      element.placeholder = translations[lang][key]
    }
  })

  // Update options in select elements
  document.querySelectorAll("select").forEach((select) => {
    Array.from(select.options).forEach((option) => {
      const translateKey = option.getAttribute("data-translate")
      if (translateKey && translations[lang][translateKey]) {
        option.textContent = translations[lang][translateKey]
      }
    })
  })
}

// Function to get translation for a key
function getTranslation(key) {
  if (translations[currentLanguage] && translations[currentLanguage][key]) {
    return translations[currentLanguage][key]
  }
  // Fallback to English
  return translations["en"][key] || key
}

// Event listener for language changes
document.addEventListener("DOMContentLoaded", () => {
  const languageSelect = document.getElementById("language")
  if (languageSelect) {
    languageSelect.addEventListener("change", (e) => {
      const selectedLanguage = e.target.value
      const langCode = languageCodeMap[selectedLanguage] || "en"
      updateLanguage(langCode)
    })
  }

  // Initialize with default language
  updateLanguage(currentLanguage)
})

// Export functions for use in other scripts
window.i18n = {
  updateLanguage,
  getTranslation,
  getCurrentLanguage: () => currentLanguage,
}